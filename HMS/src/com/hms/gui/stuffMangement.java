    public void loadCity() {
        try {
            String sql = "SELECT * FROM city";
            ResultSet rs = db.search(sql);
            Vector v = new Vector();
             v.add("Select");
            while (rs.next()) {
                String data = rs.getString("id") + "_" + rs.getString("name");
                v.add(data);
            }
            jComboBox1.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
