    private void genarateSID() {
        try {
            String sql = "SELECT COUNT(*) AS stuff_count FROM stuff";
            ResultSet rs = db.search(sql);
            if (rs.next()){
                int count = rs.getInt("stuff_count");
                String sid = "SID" + (++count);
                txt_sid.setText(sid);
            }
            txt_fname.grabFocus();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

