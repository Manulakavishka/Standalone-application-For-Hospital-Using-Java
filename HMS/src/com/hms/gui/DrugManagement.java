    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            String drugid = txt_rug_id.getText();
            
            String searchSql = "SELECT * FROM drug WHERE drug_id = '"+drugid+"'";
            ResultSet rs = db.search(searchSql);
            if (rs.next()) {
                    txt_name.setText(rs.getString("name"));
                    txt_description.setText(rs.getString("description"));
                    txt_cost.setText(rs.getString("cost"));
                    txt_qty.setText(rs.getString("qty"));
                    
                    jDateChooser1.setDate(rs.getDate("mfd"));
                    jDateChooser3.setDate(rs.getDate("exd"));
                    
                    String stat = rs.getString("status_id");
                    if (stat.equals("1")) {
                        rdo_a.setSelected(true);
                    } else {
                        rdo_i.setSelected(true);
                    }
                
            } else {
                JOptionPane.showMessageDialog(this, "DrugID Not Defind","warning", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

