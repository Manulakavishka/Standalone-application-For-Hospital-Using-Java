    private void genarateDrugID() {
        try {
            String sql = "SELECT COUNT(*) AS countDrugID FROM drug";
            ResultSet rs = db.search(sql);
            if (rs.next()) {
                int count = rs.getInt("countDrugID");
                String DrugID = "DrugID" + (++count);
                txt_rug_id.setText(DrugID);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

