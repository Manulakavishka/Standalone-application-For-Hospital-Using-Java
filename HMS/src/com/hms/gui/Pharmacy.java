    private void loadRid() {
        try {
            String sql = "SELECT * FROM opd_reception WHERE stat = '2'";
            ResultSet rs = db.search(sql);
            Vector v = new Vector();
            v.add("Select");
            while (rs.next()) {
                String data = rs.getString("RID");
                v.add(data);
            }
            combo_rid.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

