    private void combo_ridActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_ridActionPerformed
        try {
            String rid = combo_rid.getSelectedItem().toString();
            if (!rid.equals("Select")) {
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                String sql = "SELECT * FROM treatment WHERE Receipt_id = '"+rid+"' && status_id = '1'";
                ResultSet rs = db.search(sql);
                dtm.setRowCount(0);
                while (rs.next()) {
                    Vector v = new Vector();
                    v.add(rs.getString("Receipt_id"));
                    v.add(rs.getString("treatment"));
                    String sqlTreatment = "SELECT name FROM drug WHERE drug_id = '"+rs.getString("treatment")+"'";
                    ResultSet rsTreatment = db.search(sqlTreatment);
                    if (rsTreatment.next())
                    v.add(rsTreatment.getString("name"));
                    v.add(rs.getString("qty"));
                    dtm.addRow(v);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please Select RID!","warning",JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_combo_ridActionPerformed

