    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            String parid = combo_rid.getSelectedItem().toString();
            String symptoms = txt_symptoms.getText();
            String treatment = txt_treatment.getText();

            String searchSql = "SELECT * FROM ipd_doctor WHERE PARID = '"+parid+"'";
            ResultSet rs = db.search(searchSql);
            if (!parid.equals("Select") && !symptoms.trim().equals("") &&
                symptoms != null && treatment != null && !treatment.trim().equals("")) {

                rs.next();

                    String DOCsql = "INSERT INTO ipd_doctor (`PARID`,`symptoms`,`treatment`,`doctor`,`date`) VALUES ('"+parid+"','"+symptoms+"','"+treatment+"','"+jLabel8.getText()+"','"+jLabel14.getText()+"')";
                    db.push(DOCsql);

                    String ridStatUpdateSql = "UPDATE ipd_p_a_registration SET status_id = '2' WHERE PARID = '"+parid+"'";
                    db.push(ridStatUpdateSql);

                    JOptionPane.showMessageDialog(this, "Success","success",JOptionPane.INFORMATION_MESSAGE);

                    clearFields();
                    loadDischarge();

                

            } else {
                JOptionPane.showMessageDialog(this, "Please Fill All The Fields!","warning", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

