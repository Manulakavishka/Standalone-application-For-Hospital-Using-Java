    private void loadPARID() {
        try {
            String sql =   "SELECT * FROM ipd_p_a_registration WHERE status_id in ('3', '2' )";
            ResultSet rs = db.search(sql);
            Vector v = new Vector();
            v.add("Select");
            while (rs.next()) {
                String data = rs.getString("PARID");
                v.add(data);
            }
            combo_rid.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

