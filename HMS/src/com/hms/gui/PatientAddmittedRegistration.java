    private void txt_contactKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_contactKeyTyped
        String mobile = txt_contact.getText();
        String text = mobile + evt.getKeyChar();
        
        if (text.length() == 1) {
            if (!text.equals("0")) {
                evt.consume();
            }
        } else if (text.length() == 2) {
            if (!text.equals("07")) {
                evt.consume();
            }
        } else if (text.length() == 3) {
            
            if (!Pattern.compile("07[1245678]").matcher((text)).matches()) {
                
                evt.consume();
            }
        } else if (text.length() <= 10) {
            if (!Pattern.compile("07[1245678][0-9]+").matcher((text)).matches()) {
                
                evt.consume();
            }            
            
        } else {
            
            evt.consume();
        }
    }//GEN-LAST:event_txt_contactKeyTyped

