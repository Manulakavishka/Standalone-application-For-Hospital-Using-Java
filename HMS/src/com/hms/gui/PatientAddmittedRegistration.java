    private void genaratePARID() {
        try {
            String sql = "SELECT COUNT(*) AS countPARID FROM ipd_p_a_registration";
            ResultSet rs = db.search(sql);
            if (rs.next()){
                int count = rs.getInt("countPARID");
                String PARID = "PARID" + (++count);
                txt_parid.setText(PARID);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadNIC() {
        try {
            String sql = "SELECT * FROM patient WHERE nic IN (SELECT patient_nic FROM ipd_doc_patient WHERE status_id = '1') AND status_id = '1'";
            ResultSet rs = db.search(sql);
            Vector v = new Vector();
            v.add("Select");
            while (rs.next()) {
                String nic = rs.getString("nic") + "_" + rs.getString("fname") + " " + rs.getString("lname");
                v.add(nic);
            }
            combo_nic.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
        }
    }

    private void loadRoom() {
        try {
            String sql = "SELECT * FROM room WHERE status_id = '1' AND qty != '0'";
            ResultSet rs = db.search(sql);
            Vector v = new Vector();
            v.add("Select");
            while (rs.next()) {
                String data = rs.getString("room_no") + "_"+ " room_type : " + rs.getString("room_type") + " dayprice : " + rs.getString("dayprice") + " qty : " + rs.getString("qty");
                v.add(data);
            }
            combo_room.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

