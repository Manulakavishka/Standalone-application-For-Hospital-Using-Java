    private void loading() {
        
        frame = this;
        Thread thread = new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 0; i < 101; i++){
        jProgressBar1.setValue(i);
                try {
                    Thread.sleep(20);
                } catch (Exception e) {
                }
                if (i == 5) {
                    File file = new File("C:/system.cfg");
                    if (!file.exists()) {
                        JOptionPane.showMessageDialog(frame, "System.cfg File not exists..!!");
                        break;
                    }
                }
                if (i == 100) {
                    Main homeView = new Main();
                homeView.setVisible(true);
                dispose();
                }
            }
            }
        });
        thread.start();
    }
