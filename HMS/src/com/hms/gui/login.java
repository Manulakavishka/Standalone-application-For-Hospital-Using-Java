    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         try {
            String username = txt_username.getText();
            String password = new String(txt_password.getPassword());
            Connection connection = db.getDBConnection();
            String sql = "select * from system_user where username=? and password=? AND status_id = '1'";
            PreparedStatement ps = connection.prepareStatement(sql);
            ps.setString(1, username);
            String encPw = MD5.getMd5(password);
            ps.setString(2, encPw);
            ResultSet rs = ps.executeQuery();
            if (rs.next()){
                SystemConfigData.setActiveUser(username);
                SystemConfigData.setUserType(rs.getString("staff_type_id"));
                loading();
            } else {
                JOptionPane.showMessageDialog(this, "Invalid Username Or Password","warning", JOptionPane.WARNING_MESSAGE);
            }      
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

