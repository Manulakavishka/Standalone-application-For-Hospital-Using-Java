    private void genarateRID() {
        try {
            String sql = "SELECT COUNT(*) AS countRID FROM opd_reception";
            ResultSet rs = db.search(sql);
            if (rs.next()) {
                int count = rs.getInt("countRID");
                String RID = "RID" + (++count);
                txt_rid.setText(RID);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadPID() {
        try {
            String sql = "SELECT * FROM patient WHERE status_id = '1'";
            ResultSet rs = db.search(sql);
            Vector v  = new Vector();
            v.add("Select");
            while (rs.next()){
                String data = rs.getString("nic") + "_" + rs.getString("fname")+ " " + rs.getString("lname");
                v.add(data);
            }
            combo_pid.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadDID() {
        try {
            String sql = "SELECT * FROM stuff WHERE status_id = '1' AND sid IN (SELECT sid FROM system_user WHERE staff_type_id = '2')";
            ResultSet rs = db.search(sql);
            Vector v = new Vector();
            v.add("Select");
            while (rs.next()) {
                String data = rs.getString("sid") + "_" + "DR. " + rs.getString("fname") + " " + rs.getString("lname");
                v.add(data);
            }
            combo_did.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadDCharge() {
        try {
            String sql = "SELECT * FROM doctor_charge WHERE status_id = '1'";
            ResultSet rs = db.search(sql);
            Vector v = new Vector();
            v.add("Select");
            while (rs.next()) {
                String data = rs.getString("code") + "_" + rs.getString("charge");
                v.add(data);
            }
            combo_dCharge1.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
