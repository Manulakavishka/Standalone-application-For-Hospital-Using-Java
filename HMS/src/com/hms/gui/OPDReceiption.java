    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String rid = txt_rid.getText();
            String pid = combo_pid.getSelectedItem().toString().split("_")[0];
            String did = combo_did.getSelectedItem().toString().split("_")[0];
            String dcharge = combo_dCharge1.getSelectedItem().toString().split("_")[0];
            String uname =labal_un.getText();
            String date = jLabel14.getText();
            String searchSql = "SELECT RID FROM opd_reception WHERE RID = '"+rid+"'";
            ResultSet rs = db.search(searchSql);
            if (!rid.equals("") && !pid.equals("Select") && !did.equals("Select") && !dcharge.equals("Select") && !uname.equals("")){
                if (!rs.next()) {
                    String sql = "INSERT INTO opd_reception VALUES ('"+rid+"','"+pid+"','"+did+"','"+dcharge+"','"+date+"','"+uname+"','3')";
                    db.push(sql);
                    
                    JOptionPane.showMessageDialog(this, "Success!","success",JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "RID allready Defined","warning",JOptionPane.WARNING_MESSAGE);
            }
            } else {
                JOptionPane.showMessageDialog(this, "please fill All the fields!","warning",JOptionPane.WARNING_MESSAGE);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       try {
            String rid = txt_rid.getText();
           String sql = "SELECT RID FROM opd_reception WHERE RID = '"+rid+"'";
           ResultSet rs = db.search(sql);
           
           if (rs.next()) {
              
            String path = SystemVariableList.REPORT_BASE_URL+"printRecept.jasper";
//            InputStream inputStream = new FileInputStream(path);
            HashMap params = new HashMap();
            params.put("rid", rid);
            
            JasperPrint fillReport = JasperFillManager.fillReport(path, params, db.getDBConnection());
            JasperViewer.viewReport(fillReport,false);
            
            genarateRID();
            
           } else {
               JOptionPane.showMessageDialog(this, "RID Does Not Exist!","warning",JOptionPane.WARNING_MESSAGE);
           }
           
           
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

