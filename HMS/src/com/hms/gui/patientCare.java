private void loadPARID() {
        try {
            String searchSql = "SELECT * FROM ipd_p_a_registration WHERE status_id = '2'";
            ResultSet rs = db.search(searchSql);
            Vector v = new Vector();
            v.add("Select");
            while (rs.next()){
                v.add(rs.getString("PARID"));
            }
            jComboBox1.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
