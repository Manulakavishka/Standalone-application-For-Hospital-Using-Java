    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        try {
            String PARID = jComboBox1.getSelectedItem().toString();
            if (!PARID.equals("Select")) {
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                
                dtm.setRowCount(0);
                String sql = "SELECT * FROM ipd_treatment WHERE PARID = '"+PARID+"' && status_id = '1'";
                ResultSet rs = db.search(sql);
                dtm.setRowCount(0);
                while (rs.next()) {
                    Vector v = new Vector();
                    v.add(rs.getString("IPDTID"));
                    v.add(rs.getString("treatment"));
                    String sqlTreatment = "SELECT name FROM drug WHERE drug_id = '"+rs.getString("treatment")+"'";
                    ResultSet rsTreatment = db.search(sqlTreatment);
                    if (rsTreatment.next())
                    v.add(rsTreatment.getString("name"));
                    v.add(rs.getString("qty"));
                    
                   String sqlTime = "SELECT time FROM treatment_times WHERE code = '"+rs.getString("times")+"'";
                    ResultSet rsTime = db.search(sqlTime);
                    if (rsTime.next())
                    v.add(rsTime.getString("time"));
                    dtm.addRow(v);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please Select RID!","warning",JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            int selectRow = jTable1.getSelectedRow();
            String data = (String) dtm.getValueAt(selectRow, 0);
            String sql = "UPDATE ipd_treatment SET status_id = '2' WHERE IPDTID = '"+data+"'";
            db.push(sql);
            loadPARID();
            JOptionPane.showMessageDialog(this, "Success","success",JOptionPane.INFORMATION_MESSAGE);
            dtm.removeRow(selectRow);
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(this, "please Select Row","warning",JOptionPane.WARNING_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

